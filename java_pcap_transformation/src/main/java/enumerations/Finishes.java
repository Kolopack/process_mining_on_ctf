package enumerations;

/**
 * The enum Finishes.
 * For labelling the different sections of the TCP-finishing process.
 */
public enum Finishes {
    /**
     * First finishes. (First packet)
     */
    FIRST,
    /**
     * Second finishes. (Second packet)
     */
    SECOND,
    /**
     * Third finishes. (Third packet)
     */
    THIRD
}
